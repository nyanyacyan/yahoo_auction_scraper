from typing import Final  # Final は「再代入しない意図の定数」であることを型ヒントで示すために使う
import re  # 正規表現を扱う標準ライブラリ。パターンのコンパイルに使用する
    # 空行: ここから ct（カラット）数値を抽出するための正規表現リストを定義する

# 例: 「0.502ct」「【0.5ct】」などから数値を取る  # 対応したいタイトル表記例を説明している

CT_PATTERNS: Final[list[re.Pattern]] = [  # ct 数値を検出するための re.Pattern オブジェクトの配列（変更しない前提）
    re.compile(r"(\d+(?:\.\d+)?)\s*ct", re.IGNORECASE),  # 「数値 + 任意空白 + ct」を大小無視で検出（例: 0.5ct / 1 ct）
    re.compile(r"【\s*(\d+(?:\.\d+)?)\s*ct\s*】", re.IGNORECASE),  # 全角カッコ内の「数値 + ct」を検出（例: 【 0.5ct 】）
]  # パターンリストの終端（後段の抽出処理がこの順にマッチを試す）
