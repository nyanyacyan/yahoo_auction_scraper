# ---- 正規表現（必要に応じて編集） ----  # 想定される表記ゆれに合わせてパターンを切り替える
RE_YMD_SLASH = r"^\d{4}/\d{1,2}/\d{1,2}$"           # 例: 2025/08/10（年/月/日をスラッシュ区切りで厳密一致）
RE_YMD_DASH  = r"^\d{4}-\d{1,2}-\d{1,2}$"           # 例: 2025-08-10（年-月-日をハイフン区切りで厳密一致）
RE_MD_HM     = r"(\d{1,2})/(\d{1,2}) (\d{1,2}):(\d{1,2})"  # 例: 6/28 23:59（年なし→別ロジックで年補完）
RE_JP_FULL   = r"(\d{1,2})月(\d{1,2})日.*?(\d{1,2})時(\d{1,2})分"  # 例: 6月28日(土) 23時59分（日本語表記）

# 空行: パターンを使う/使わないを切り替えるフラグ群

# ---- 有効/無効 切替 ----  # 特定の形式を無効化したいときに False にする
ENABLE_YMD_SLASH = True  # スラッシュ形式(YYYY/MM/DD)を許可
ENABLE_YMD_DASH  = True  # ハイフン形式(YYYY-MM-DD)を許可
ENABLE_MD_HM     = True  # 月日+時刻(M/D H:M)を許可（年は後段で補完）
ENABLE_JP_FULL   = True  # 日本語形式(～月～日 ～時～分)を許可（年は後段で補完）

# 空行: 年が書かれていない表記に対して、どの年を当てるかの方針

# ---- 年の補完ポリシー ----  # 年なし入力へ適用する「今年/翌年」の決め方
# "current"       : 年なし表記は常に「今年」を補完  # シンプルで予測可能
# "smart_rollover": 今年のその日付が「今より十分過去」なら翌年へ繰り上げ（年またぎに強い）  # 年末年始の誤判定を軽減
YEAR_POLICY = "current"  # or "smart_rollover"  # 既定は常に今年で補完

# 空行: smart_rollover 使用時の基準日数

# smart_rollover 用の閾値（日数）  # 「十分過去」の基準（この日数より過去なら翌年扱い）
SMART_ROLLOVER_THRESHOLD_DAYS = 45  # 例: 45日より前なら翌年へ補完
