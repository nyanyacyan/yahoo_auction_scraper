from typing import Final  # 定数として扱う意図を型で明示するために使用（変更しない前提）
    # 空行: ここから実際に使用するテンプレート定数を定義

# Google Sheets IMAGE 関数テンプレ  # {url} を差し込んで画像をセルに表示（モード4はサイズ指定）
IMAGE_FORMULA: Final[str] = '=IMAGE("{url}", 4, 80, 80)'  # 幅80px・高さ80pxで表示する既定式

# シートで自動変換を避けるための先頭プレフィックス  # 先頭に'を付けると数値や日付が文字列として扱われる
TEXT_PREFIX: Final[str] = "'"  # 例えば "2025-01-01" をそのまま文字列で保持したい場合に利用

# input_date のフォールバックフォーマット  # 入力日時を文字列化する際の既定strftime形式
INPUT_DATE_FMT: Final[str] = "%Y-%m-%d"  # 例: 2025-08-10 のように日付のみを記録
